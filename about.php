<h3>About Us</h3>
<p>The project’s main purpose is to easily access the data related to electricity supply in a particular area. This involves viewing consumer details and enabling the payment of electricity bills digitally. The project focuses on the data related to the electricity consumers within the  Electricity Board.<br>The basic objective of the project on electricity board management is to store and access the data related to the consumers along with their respective providers, this includes the consumer details under different supply chains, the employee details of the respective supply chains, the billing details of different consumers such as (billing date, amount, due date, payment status, previous balances), the entry related to new connections within a particular locality, service requests put up by the consumers and details related to individual supply chains.</p>
